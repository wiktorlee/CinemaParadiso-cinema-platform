package pl.cinemaparadiso.enums;

/**
 * Dni tygodnia dla harmonogramów seansów
 * Używane w ScreeningSchedule do automatycznego generowania seansów
 * 
 * Przykład: PIĄTEK oznacza, że seans będzie generowany w każdy piątek
 */
public enum ScheduleDayOfWeek {
    MONDAY,    // Poniedziałek
    TUESDAY,   // Wtorek
    WEDNESDAY, // Środa
    THURSDAY,  // Czwartek
    FRIDAY,    // Piątek
    SATURDAY,  // Sobota
    SUNDAY     // Niedziela
}

